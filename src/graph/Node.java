package graph;

public class Node {
	protected float x;
	protected float y;
	

	public Node(float cx, float cy){
		x= cx;
		y= cy;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
	
	
}
